# List fruits
fruits = ["strawberry", "pomegranate", "mango", "grape"]

# List veggies
veggies = ["avocado", "tomato", "onion", "asparagus"]

# For loop fruits
print("My fav fruits are:")
for fruit in fruits:
    print(fruit)

# For loop veggies
print("\nMy fav veggies are:")
for veg in veggies:
    print(veg)